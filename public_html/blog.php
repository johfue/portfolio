        <h1>My secret</h1>
        <br>
        <p>My secret is that I did not find out about MAIP until about a week before the deadline, and the application for art director fellows was very intensive. But I found an alternative way in, applying as a creative technologist. Because here’s the thing about me, I’m an art director who’s really into web design. And not just that I’m a web designer who can code and thinks the way HTML and CSS natively work is really, really cool. I am a front-end developer. That sounds enough like a creative technologist to me, and MAIP seemed to agree.</p>
<br>
<p>I would find out that I was only one of two fellows out of the entire MAIP class to be accepted as creative technologists. I was quite the unicorn.</p>
<br>
<p>The 2nd phase of the program broke us into our respective disciplines. Us two creative technologists were lumped with the UI/UX folks. Together we formed a group of less than 15. Our trainings focused on UI and UX. While not what I was expecting, were still interesting. They introduced me to the field of product design, which meshed well with philosophy towards advertising. I learned about the Kano model for prioritizing product features.</p>
<br>
<p>Not only that but I got introduced to some talented UX/UI designers. All of whom are super motivated and driven people. They added a lot to the experience. During every session they were asking questions I didn't even know I wanted the answer to. </p>
<br>
<p>Ultimately however through this experience, I learned that creative technology is not for me. In theory it is. The way it was sold to me is that in modern agencies on the cutting edge, the typical AD/CW team has gone the way of the dodo. Nowadays creative teams are composed of multiple teams of people of different disciplines, developers, designers, writers, are working on concepting together. I learned that is not the case, creative technologists often are not included in the concepting stage. Oftentimes they are relegated to just being producers. Which is not as exciting to me.</p>
